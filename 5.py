#En python, IF es una estructura de control de flujo que se utiliza para tomar decisiones en el código.
#Se realiza un bloque de codigo si la condición es falsa o verdadera

#Ejemplo

numero = float(input("Ingrese un número: "))


if numero > 0:
    print("El número ingresado es positivo.")
elif numero < 0:
    print("El número ingresado es negativo.")
else:
    print("El número ingresado es cero.")