#La diferencia principal entre lista y tupla en python es que las listas se utilizan cuando se requiere 
# una estructura de datos mutables o que se puedan cambiar, y las tuplas se usas cuando l estructura requiere
# ser inmutable y que los datos no se puedan modifica.

# Tambien se direncian en la sintaxis al momento de definirse, ya que, las listas se definen entre [] y las 
# tuplas entre ()

#Ejemplo

#lista 
numeros= [1, 2, 3]

print("segundo numero", numeros[1])
#tuplas
letras = ("a", "b", "c")

print("primera letra", letras[0])