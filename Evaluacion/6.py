#En python un bubcle for es  es una estructura de control que se utiliza para iterar sobre una 
#secuencia y ejecutar un bloque de código para cada elemento de la secuencia.

#ejemplo

numeros = [1, 5, 2, 4, 6]

suma = 0

for numero in numeros:
    suma += numero

print("La suma de los números en la lista es:", suma)